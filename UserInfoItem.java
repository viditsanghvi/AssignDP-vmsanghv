public class UserInfoItem {
    String username;

    public void setUsername(String username) {
        this.username = username;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    String userType;

    public String getUsername() {
        return username;
    }

    public String getUserType() {
        return userType;
    }
}
